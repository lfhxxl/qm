package top.book.pojo;

public class book {

	private int Id;
	private String Name;
	private int price;

	public void setId(int Id) {
		this.Id = Id;
	}

	public int getId() {
		return Id;
	}

	public void setName(String Name) {
		this.Name = Name;
	}

	public String getName() {
		return Name;
	}

	public void setprice(int price) {
		this.price = price;
	}

	public int getprice() {
		return price;
	}

}